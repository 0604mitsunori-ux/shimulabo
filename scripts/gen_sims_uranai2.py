def seed_sim(id, emoji, title, desc, ogtitle, ogdesc, h1, lead, inputs_html, seed_ids, results, rlabel, sharetpl, article):
    # results: [[emoji,name,desc],...]  seed_ids: 入力欄のidリスト（値を連結してhash）
    js = ('  const RES=' + json.dumps(results, ensure_ascii=False) + ';\n'
          '  const SEEDS=' + json.dumps(seed_ids, ensure_ascii=False) + ', RLABEL=' + json.dumps(rlabel, ensure_ascii=False) + ', SHARETPL=' + json.dumps(sharetpl, ensure_ascii=False) + ';\n'
          + r'''  function hash(s){let h=2166136261;for(let i=0;i<s.length;i++){h^=s.charCodeAt(i);h=Math.imul(h,16777619);}return h>>>0;}
  function calc(){let key='';for(const id of SEEDS){const el=$(id);if(el)key+=(el.value||'')+'|';}
    if(key.replace(/\|/g,'').trim()===''){alert('入力してね');return;}
    const r=RES[hash(key)%RES.length];
    $('emoji').textContent=r[0];$('big').textContent=r[1];$('sub').textContent='占い結果';$('desc').textContent='✨ '+r[2];
    SHARE=SHARETPL.replace('{name}',r[1]);show();}
''')
    SIMS.append(dict(id=id, emoji=emoji, cat=CAT, title=title, desc=desc, ogtitle=ogtitle, ogdesc=ogdesc, h1=h1, lead=lead,
        inputs=inputs_html, result=RESULT_HTML.replace('__RLABEL__',rlabel), visual='', article=article, js=js))

